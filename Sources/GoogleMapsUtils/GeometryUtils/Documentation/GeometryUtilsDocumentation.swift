/// # GeometryUtils Module
///
/// Mathematical utilities and extensions for Google Maps geometry calculations.
///
/// ## Overview
///
/// GeometryUtils provides mathematical functions and coordinate transformations
/// for geometric calculations on maps.
///
/// ```swift
/// let mapPoint = coordinate.mapPoint
/// let distance = point1.distance(to: point2)
/// let interpolated = MapPoint.interpolate(from: start, to: end, fraction: 0.5)
/// ```
///
/// ## Topics
///
/// ### Coordinate Extensions
/// - ``GoogleMaps/GMSPath``
/// - ``GoogleMaps/GMSPolygon``
/// - ``GoogleMaps/GMSPolyline``
///
///
/// ### Utilities
/// - ``MapPoint``
///
/// > Warning: This is a documentation-only namespace. Do not instantiate or use this struct in your code.
public struct GeometryUtilsDocumentation {

    /// Private initializer to prevent instantiation.
    ///
    /// This struct exists solely for documentation organization and should never be instantiated.
    /// It serves as a namespace to group GeometryUtils module documentation in one place.
    private init() {}
}

